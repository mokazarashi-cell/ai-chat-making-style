function renderRatings(container, ratings) {
    container.innerHTML = "";
    if (!ratings) return;

    ratings.forEach(item => {
        const row = document.createElement("div");
        row.classList.add("rating-row");

        const label = document.createElement("span");
        label.classList.add("rating-label");
        label.textContent = item.label;

        const stars = document.createElement("span");
        stars.classList.add("rating-stars");
        stars.setAttribute("aria-label", `${item.label}: ${item.value}/5`);
        stars.textContent = "★".repeat(item.value) + "☆".repeat(5 - item.value);

        row.appendChild(label);
        row.appendChild(stars);
        container.appendChild(row);
    });
}

